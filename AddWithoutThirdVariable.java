import java.util.*;
	class AddWithoutThirdVariable {
		public static void main(String[] args) {
		        Scanner sc=new Scanner(System.in);
		        System.out.println("ENTER 1ST NUMBER:");
		        int x=sc.nextInt(); //TAKING THE 1ST NUMBER INPUT FROM USER
		        System.out.println("ENTER 2ND NUMBER:");
		        int y=sc.nextInt(); //TAKING THE 2ND NUMBER INPUT FROM USER
		        x+=y; //ADDING THE TWO NUMBERS AND STORING IT IN THE SAME VARIABLE IN WHICH THE INPUT IS TAKEN.
		        System.out.println("SUM OF THE GIVEN 2 NUMBERS= "+x); 	//GIVING THE OUTPUT	
	}
}
